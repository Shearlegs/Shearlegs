CREATE TABLE dbo.Results
(
	Id INT IDENTITY(1, 1) NOT NULL,
	VersionId INT NOT NULL CONSTRAINT FK_Results_VersionId FOREIGN KEY REFERENCES dbo.Versions(Id),
	UserId INT NOT NULL CONSTRAINT FK_Results_UserId FOREIGN KEY REFERENCES dbo.Users(Id),
	ParametersJson NVARCHAR(MAX) NOT NULL,
	[Status] TINYINT NOT NULL,
	ResultJson NVARCHAR(MAX) NULL,
	ResultType VARCHAR(255) NOT NULL,
	CreateDate DATETIME2(0) NOT NULL CONSTRAINT DF_Results_CreateDate DEFAULT SYSDATETIME(),
	CONSTRAINT PK_Results PRIMARY KEY (Id)
);